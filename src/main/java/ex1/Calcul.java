package ex1;

/**
 * Classe qui réalise des opérations arythmétiques
 * 
 * @author DIGINAMIC
 */
public class Calcul {

	/**
	 * Additionner deux nombres
	 * 
	 * @param n1 premier nombre
	 * @param n2 deuxième nombre
	 * @return int résultat addition
	 */
	public int addition(int n1, int n2) {
		return n1 + n2;
	}

	/**
	 * Soustraire deux nombres
	 * 
	 * @param n1 premier nombre
	 * @param n2 deuxième nombre
	 * @return int résultat soustraction
	 */
	public int soustraction(int n1, int n2) {
		return n1 - n2;
	}
}
